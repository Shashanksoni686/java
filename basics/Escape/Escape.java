public class Escape{
    public static void main(String[] args){
        System.out.println("Hello \"Shashank\"... ");//double quote
        System.out.println("Hello \\Shashank\\... ");//back slash
        System.out.println("Hello \nShashank... ");//new line
        System.out.println("Hello \'Shashank\'... ");//single quote
        System.out.println("Hello \bShashank... ");//remove space
        System.out.println("Hello \tShashank... ");//space of tab
    }
}